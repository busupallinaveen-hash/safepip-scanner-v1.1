def _windows_get_user_path() -> str:
    try:
        import winreg
        key = winreg.OpenKey(winreg.HKEY_CURRENT_USER, "Environment", 0, winreg.KEY_READ)
        val, _ = winreg.QueryValueEx(key, "Path")
        winreg.CloseKey(key)
        return val
    except Exception:
        return os.environ.get("PATH", "")


def _windows_set_user_path(new_path: str) -> None:
    try:
        import winreg
        key = winreg.OpenKey(winreg.HKEY_CURRENT_USER, "Environment", 0, winreg.KEY_SET_VALUE)
        winreg.SetValueEx(key, "Path", 0, winreg.REG_EXPAND_SZ, new_path)
        winreg.CloseKey(key)
        import ctypes
        ctypes.windll.user32.SendMessageW(0xFFFF, 0x001A, 0, "Environment")
    except Exception:
        pass


def _windows_add_to_path(directory: str) -> bool:
    current = _windows_get_user_path()
    dirs = [d for d in current.split(";") if d.strip()]
    if directory in dirs:
        return False
    dirs.insert(0, directory)
    _windows_set_user_path(";".join(dirs))
    return True


def _windows_remove_from_path(directory: str) -> bool:
    current = _windows_get_user_path()
    dirs = [d for d in current.split(";") if d.strip() and d != directory]
    _windows_set_user_path(";".join(dirs))
    return True


def _windows_fix_pathext() -> bool:
    """Ensure .BAT resolves before .EXE so pip.bat intercepts pip.exe."""
    try:
        import winreg
        key = winreg.OpenKey(winreg.HKEY_CURRENT_USER, "Environment", 0,
                             winreg.KEY_READ | winreg.KEY_SET_VALUE)
        try:
            val, _ = winreg.QueryValueEx(key, "PATHEXT")
        except FileNotFoundError:
            val = os.environ.get("PATHEXT", ".COM;.EXE;.BAT;.CMD;.VBS;.VBE;.JS;.JSE;.WSF;.WSH;.MSC")
        exts = [e.upper() for e in val.split(";") if e.strip()]
        if ".BAT" in exts and ".EXE" in exts:
            if exts.index(".BAT") > exts.index(".EXE"):
                exts.remove(".BAT")
                exe_idx = exts.index(".EXE")
                exts.insert(exe_idx, ".BAT")
                new_val = ";".join(exts)
                winreg.SetValueEx(key, "PATHEXT", 0, winreg.REG_SZ, new_val)
                winreg.CloseKey(key)
                return True
        winreg.CloseKey(key)
    except Exception:
        pass
    return False


# ---------------------------------------------------------------------------
# PowerShell profiles
# ---------------------------------------------------------------------------

def _find_powershell_profiles() -> list[Path]:
    if not IS_WINDOWS:
        return []
    docs = Path.home() / "Documents"
    profiles = []
    for ps_dir in ("WindowsPowerShell", "PowerShell"):
        for profile_name in ("Microsoft.PowerShell_profile.ps1", "profile.ps1"):
            profiles.append(docs / ps_dir / profile_name)
    return profiles


def _write_ps_alias(safe_pip_exe: str, real_pip: str) -> list[Path]:
    snippet = _PS_ALIAS_TEMPLATE.format(safe_pip_exe=safe_pip_exe, real_pip=real_pip)
    written = []
    for profile in _find_powershell_profiles():
        profile.parent.mkdir(parents=True, exist_ok=True)
        existing = profile.read_text(encoding="utf-8") if profile.exists() else ""
        if "safe-pip watch" in existing:
            continue
        profile.write_text(existing + "\n" + snippet, encoding="utf-8")
        written.append(profile)
    return written


def _remove_ps_alias() -> int:
    removed = 0
    for profile in _find_powershell_profiles():
        if not profile.exists():
            continue
        content = profile.read_text(encoding="utf-8")
        if "safe-pip watch" not in content:
            continue
        lines = content.splitlines()
        out, inside = [], False
        for line in lines:
            if ">>> safe-pip watch >>>" in line:
                inside = True
            if not inside:
                out.append(line)
            if "<<< safe-pip watch <<<" in line:
                inside = False
        profile.write_text("\n".join(out), encoding="utf-8")
        removed += 1
    return removed


# ---------------------------------------------------------------------------
# CMD shims
# ---------------------------------------------------------------------------

def _write_cmd_shim(target: Path, safe_pip_exe: str, real_pip: str) -> None:
    target.parent.mkdir(parents=True, exist_ok=True)
    target.write_text(_windows_shim_content(safe_pip_exe, real_pip), encoding="utf-8")


def _remove_cmd_shim(target: Path) -> bool:
    if not target.exists():
        return False
    content = target.read_text(encoding="utf-8")
    if "safe-pip shim" not in content:
        return False
    target.unlink()
    return True


def _cmd_shim_locations(real_pip: str) -> list[Path]:
    """Return all pip.bat locations to install/remove."""
    locations = []
    # shim dir (for PATH-based interception)
    locations.append(_shim_dir() / "pip.bat")
    # beside each pip.exe in PATH
    for name in ("pip.exe", "pip3.exe"):
        found = shutil.which(name)
        if found:
            p = Path(found).parent / (Path(found).stem + ".bat")
            if p not in locations:
                locations.append(p)
    # beside real pip
    rp = Path(real_pip)
    if rp.exists():
        for stem in ("pip", "pip3"):
            p = rp.parent / f"{stem}.bat"
            if p not in locations:
                locations.append(p)
    return locations


# ---------------------------------------------------------------------------
# Unix PATH management
# ---------------------------------------------------------------------------

def _unix_rc_files() -> list[Path]:
    home = Path.home()
    candidates = [
        home / ".bashrc", home / ".bash_profile", home / ".zshrc",
        home / ".profile", home / ".config" / "fish" / "config.fish",
    ]
    return [p for p in candidates if p.exists()]


def _unix_add_to_path(directory: str) -> bool:
    marker = f'# safe-pip: PATH={directory}'
    export = f'export PATH="{directory}:$PATH"'
    for rc in _unix_rc_files():
        content = rc.read_text(encoding="utf-8")
        if marker in content:
            return False
        rc.write_text(content + f"\n{marker}\n{export}\n", encoding="utf-8")
    return True


def _unix_remove_from_path(directory: str) -> bool:
    removed = False
    marker = f'# safe-pip: PATH={directory}'
    for rc in _unix_rc_files():
        content = rc.read_text(encoding="utf-8")
        if marker not in content:
            continue
        lines = [l for l in content.splitlines()
                 if marker not in l and directory not in l]
        rc.write_text("\n".join(lines), encoding="utf-8")
        removed = True
    return removed


# ---------------------------------------------------------------------------
# Enable / Disable
# ---------------------------------------------------------------------------

def enable(force: bool = False) -> dict:
    """Install shims in all terminal environments."""
    safe_pip_exe = _find_safe_pip_exe()
    real_pip     = _find_real_pip()
    shim_d       = _shim_dir()
    shim_d.mkdir(parents=True, exist_ok=True)

    result = {
        "safe_pip_exe": safe_pip_exe,
        "real_pip":     real_pip,
        "shim_dir":     str(shim_d),
        "cmd_shims":    [],
        "ps_profiles":  [],
        "path_updated": False,
        "pathext_fixed": False,
    }

    if IS_WINDOWS:
        # CMD shims
        for loc in _cmd_shim_locations(real_pip):
            _write_cmd_shim(loc, safe_pip_exe, real_pip)
            result["cmd_shims"].append(str(loc))

        # PowerShell alias
        written = _write_ps_alias(safe_pip_exe, real_pip)
        result["ps_profiles"] = [str(p) for p in written]

        # PATH: shim dir first
        result["path_updated"] = _windows_add_to_path(str(shim_d))

        # PATHEXT: .BAT before .EXE
        result["pathext_fixed"] = _windows_fix_pathext()
    else:
        # Unix: write shim + add to PATH
        shim_path = shim_d / "pip"
        shim_path.write_text(_unix_shim_content(safe_pip_exe, real_pip), encoding="utf-8")
        shim_path.chmod(shim_path.stat().st_mode | stat.S_IEXEC | stat.S_IXGRP | stat.S_IXOTH)
        result["cmd_shims"].append(str(shim_path))
        result["path_updated"] = _unix_add_to_path(str(shim_d))

    return result


def disable() -> dict:
    """Remove all shims from all terminal environments."""
    shim_d  = _shim_dir()
    result  = {"cmd_removed": 0, "ps_removed": 0, "path_removed": False}

    if IS_WINDOWS:
        for loc in _cmd_shim_locations(_find_real_pip()):
            if _remove_cmd_shim(loc):
                result["cmd_removed"] += 1
        result["ps_removed"]   = _remove_ps_alias()
        result["path_removed"] = _windows_remove_from_path(str(shim_d))
        # Clean up shim dir
        if shim_d.exists():
            try:
                shutil.rmtree(shim_d)
            except Exception:
                pass
    else:
        shim_path = shim_d / "pip"
        if shim_path.exists():
            shim_path.unlink()
            result["cmd_removed"] = 1
        result["path_removed"] = _unix_remove_from_path(str(shim_d))

    return result


def status() -> dict:
    shim_d    = _shim_dir()
    shim_file = shim_d / ("pip.bat" if IS_WINDOWS else "pip")
    on_path   = str(shim_d).lower() in os.environ.get("PATH", "").lower()
    return {
        "active":    shim_file.exists() and on_path,
        "shim_file": str(shim_file),
        "shim_exists": shim_file.exists(),
        "on_path":   on_path,
        "platform":  "Windows" if IS_WINDOWS else platform.system(),
    }


