"""
safe-pip watch mode v1.1
Installs a pip shim that intercepts `pip install` in all terminals:
  - CMD: pip.bat shim in Scripts/ and shim dir
  - PowerShell: function pip { ... } in all PS profiles
  - Any terminal: shim dir added to PATH before real pip
"""

from __future__ import annotations

import os
import platform
import shutil
import stat
import sys
from pathlib import Path
from typing import Optional

import click
from rich.console import Console

console = Console()

IS_WINDOWS = platform.system() == "Windows"

# ---------------------------------------------------------------------------
# Paths
# ---------------------------------------------------------------------------

def _shim_dir() -> Path:
    if IS_WINDOWS:
        return Path(os.environ.get("APPDATA", Path.home() / "AppData" / "Roaming")) / "safe-pip" / "shims"
    return Path.home() / ".local" / "share" / "safe-pip" / "shims"


def _find_safe_pip_exe() -> str:
    exe = shutil.which("safe-pip")
    if exe:
        return exe
    scripts = Path(sys.executable).parent
    for name in ("safe-pip", "safe-pip.exe"):
        p = scripts / name
        if p.exists():
            return str(p)
    return "safe-pip"


def _find_real_pip() -> str:
    """Find the real pip executable, skipping any shim directory."""
    shim = str(_shim_dir()).lower()
    for candidate in ("pip", "pip3"):
        found = shutil.which(candidate)
        if found and shim not in found.lower():
            return found
    scripts = Path(sys.executable).parent
    for name in ("pip.exe", "pip", "pip3.exe", "pip3"):
        p = scripts / name
        if p.exists() and shim not in str(p).lower():
            return str(p)
    return "pip"


