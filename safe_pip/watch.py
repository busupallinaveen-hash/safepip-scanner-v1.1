# Shim content generators
# ---------------------------------------------------------------------------

def _windows_shim_content(safe_pip_exe: str, real_pip: str) -> str:
    return f"""@echo off
REM safe-pip shim — intercepts pip install and routes through safe-pip scan
REM Generated by: safe-pip watch enable
REM Remove with:  safe-pip watch disable
REM
REM Emergency bypass (CMD):        set SAFE_PIP_BYPASS=1 && pip install X && set SAFE_PIP_BYPASS=
REM Emergency bypass (PowerShell): $env:SAFE_PIP_BYPASS=1; pip install X; $env:SAFE_PIP_BYPASS=""

REM Only intercept "pip install ..." — forward everything else immediately
if /i not "%~1"=="install" goto :real_pip

REM --- Emergency bypass ---
if not "%SAFE_PIP_BYPASS%"=="" (
    echo [safe-pip] SAFE_PIP_BYPASS set -- skipping scan
    goto :real_pip
)

REM --- Bypass conditions ---
if /i "%~2"=="-e" goto :real_pip
if /i "%~2"=="--editable" goto :real_pip
if /i "%~2"=="-r" goto :real_pip
if /i "%~2"=="--requirement" goto :real_pip

echo %* | findstr /i "safe-pip safe_pip safe-pip-scanner" >nul 2>&1
if not errorlevel 1 goto :real_pip

if not exist "{safe_pip_exe}" goto :real_pip

echo %2 | findstr /r "^[.]" >nul 2>&1
if not errorlevel 1 goto :real_pip
echo %2 | findstr /i ".whl .tar.gz .zip" >nul 2>&1
if not errorlevel 1 goto :real_pip

REM --- Run safe-pip scan (called at top level — not inside if block) ---
"{safe_pip_exe}" install %2 %3 %4 %5 %6 %7 %8 %9
set SAFEPIP_RC=%ERRORLEVEL%

if %SAFEPIP_RC% neq 0 (
    echo.
    echo [safe-pip] Blocked ^(exit %SAFEPIP_RC%^). Run: safe-pip doctor
    echo [safe-pip] Bypass: set SAFE_PIP_BYPASS=1 ^&^& pip install X ^&^& set SAFE_PIP_BYPASS=
    exit /b %SAFEPIP_RC%
)

REM --- Scan passed: run real pip to actually install ---
:real_pip
{real_pip} %*
exit /b %ERRORLEVEL%
"""


def _unix_shim_content(safe_pip_exe: str, real_pip: str) -> str:
    return f"""#!/bin/sh
# safe-pip shim — intercepts pip install and routes through safe-pip scan
# Generated by: safe-pip watch enable
# Remove with:  safe-pip watch disable

# Only intercept install subcommand
if [ "$1" != "install" ]; then
    exec {real_pip} "$@"
fi

# Emergency bypass
if [ -n "$SAFE_PIP_BYPASS" ]; then
    echo "[safe-pip] SAFE_PIP_BYPASS set -- skipping scan"
    exec {real_pip} "$@"
fi

# Bypass: editable / requirements file / local path
case "$2" in
    -e|--editable|-r|--requirement) exec {real_pip} "$@" ;;
    *.whl|*.tar.gz|*.zip|./*|/*)    exec {real_pip} "$@" ;;
esac

# Run safe-pip scan
"{safe_pip_exe}" install "$@"
SAFEPIP_RC=$?
if [ "$SAFEPIP_RC" -ne 0 ]; then
    echo ""
    echo "[safe-pip] Installation blocked (exit code $SAFEPIP_RC)."
    echo "[safe-pip] Emergency bypass: SAFE_PIP_BYPASS=1 pip install <package>"
    exit $SAFEPIP_RC
fi

# Scan passed: run real pip
exec {real_pip} "$@"
"""


_PS_ALIAS_TEMPLATE = """# >>> safe-pip watch >>>
# Auto-generated by: safe-pip watch enable
# Remove with:       safe-pip watch disable
function pip {{
    if ($args.Count -gt 0 -and $args[0] -eq "install") {{
        # Run safe-pip scan first
        & "{safe_pip_exe}" install ($args | Select-Object -Skip 1)
        $rc = $LASTEXITCODE
        if ($rc -ne 0) {{
            Write-Host ""
            Write-Host "[safe-pip] Installation blocked (exit code $rc)." -ForegroundColor Red
            Write-Host "[safe-pip] Emergency bypass: `$env:SAFE_PIP_BYPASS=1; pip install <package>; `$env:SAFE_PIP_BYPASS=''" -ForegroundColor Yellow
            return
        }}
        # Scan passed: run real pip
        & "{real_pip}" @args
    }} else {{
        & "{real_pip}" @args
    }}
}}
# <<< safe-pip watch <<<
"""


