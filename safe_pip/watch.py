@click.group("watch")
def watch_cmd():
    """Manage pip interception across all terminal environments."""
    pass


@watch_cmd.command("enable")
def watch_enable():
    """Install pip shim in CMD, PowerShell, and PATH."""
    console.print("\n[bold]safe-pip watch[/bold] — enabling\n")
    r = enable()

    console.print(f"  [green]✓[/green] Shim installed → {r['shim_dir']}")
    console.print(f"  [green]✓[/green] Real pip       → {r['real_pip']}")
    if r.get("path_updated"):
        console.print("  [green]✓[/green] Shim directory added to your PATH.")
    if r.get("pathext_fixed"):
        console.print("  [green]✓[/green] PATHEXT updated — .BAT now resolves before .EXE.")
    if r.get("ps_profiles"):
        console.print(f"  [green]✓[/green] PowerShell alias written to {len(r['ps_profiles'])} profile(s):")
        for p in r["ps_profiles"]:
            console.print(f"     {p}")
    if r.get("cmd_shims"):
        console.print(f"\n  CMD coverage:")
        for s in r["cmd_shims"]:
            console.print(f"  [green]✓[/green] pip.bat installed → {s}")

    console.print("\n  [yellow]⚠[/yellow]  Open a new terminal for changes to take effect.")
    console.print("\n  Every [bold]pip install X[/bold] will be scanned.")
    console.print("  Disable at any time with: [bold]safe-pip watch disable[/bold]\n")


@watch_cmd.command("disable")
def watch_disable():
    """Remove pip shim from all terminals."""
    console.print("\n[bold]safe-pip watch[/bold] — disabling\n")
    r = disable()

    if r["cmd_removed"]:
        console.print(f"  [green]✓[/green] CMD shim(s) removed from {r['cmd_removed']} location(s).")
    else:
        console.print("  No shim found (already disabled).")
    if r.get("ps_removed"):
        console.print(f"  [green]✓[/green] PowerShell alias removed from {r['ps_removed']} profile(s).")
    if r.get("path_removed"):
        console.print("  [green]✓[/green] Shim directory removed from PATH.")

    console.print("\n  Watch mode is now off. pip install commands run normally.")
    console.print("  [yellow]⚠[/yellow]  Open a new terminal to apply the PATH change.\n")


@watch_cmd.command("status")
def watch_status():
    """Show watch mode status."""
    s = status()
    shim_exists = s["shim_exists"]
    on_path     = s["on_path"]

    # If shim exists but not on PATH, the user is in the same terminal
    # they ran `watch enable` in — PATH hasn't refreshed yet.
    if shim_exists and not on_path:
        state = "[yellow]PENDING[/yellow] — open a new terminal to activate"
    elif s["active"]:
        state = "[green]ACTIVE[/green]"
    else:
        state = "[dim]INACTIVE[/dim]"

    console.print(f"\n  [bold]Watch mode:[/bold] {state}")
    console.print(f"  Platform   : {s['platform']}")
    console.print(f"  Shim file  : {s['shim_file']}")
    console.print(f"  Shim exists: {'yes' if shim_exists else 'no'}")
    console.print(f"  On PATH    : {'yes' if on_path else 'no'}")

    if shim_exists and not on_path:
        console.print()
        console.print("  [yellow]ℹ[/yellow]  Shim is installed but this terminal was opened before")
        console.print("     [yellow]safe-pip watch enable[/yellow] ran. Open a [bold]new[/bold] terminal")
        console.print("     and run [bold]pip install X[/bold] — it will be intercepted.")
    elif not shim_exists:
        console.print()
        console.print("  Run [bold]safe-pip watch enable[/bold] to activate.")
    console.print()
